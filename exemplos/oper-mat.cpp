//oper-mat.c

#include <iostream>
using namespace std;

int main(){

    double x, y;
    x=2, y=3;

    cout << "x + y = " << x+y << "\n" << endl;
    cout << "x - y = " << x-y << "\n" << endl;
    cout << "x * y = " << x*y << "\n" << endl;
    cout << "x / y = " << x/y << "\n" << endl;
    
    return 0;
}